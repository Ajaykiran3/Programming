#include <stdio.h>
int main(){
    int a;
    printf("Enter the Value of a : \n");
    scanf("%d",&a);
    printf("The Number is : %d\n",a);
    float b;
    printf("Enter the Value of b : \n");
    scanf("%f",&b);
    printf("The Number is : %f\n",b);
    char c;
    printf("Enter the Value of a : \n");
    scanf("%c",&c);
    printf("The Number is : %c\n",c);
    double d;
    printf("Enter the Value of d : \n");
    scanf("%lf",&d);
    printf("The Number is : %lf\n",d);
    return 0;
}